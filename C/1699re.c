#include <stdio.h>  // 제곱수의 합  // 처음부터 다시

#define min(a, b) a > b ? b : a

int dp[100001];

int main()
{
    int n;
    scanf("%d ", &n);

    for (int i = 1; i <= n; i++)
    {
        dp[i] = i;
        for (int j = 1; j * j <= i; j++)
        {
            dp[i] = min(dp[i], dp[i - j * j] + 1);
        }
    }

	return 0;
}

